pub const Font = struct {
    // id as defined in pdf resources. needs to match declaration order in layouter.
    id: u8,
    name: []const u8,
    advance: u32,
    descent: u32,
    gap: u32,
    units_per_em: u32,
    max: u16,
    glyph_advances: []const u16,

    pub fn getLineHeight(self: *const Font, font_size: u16) u16 {
        return @intCast(font_size * (self.advance + self.descent + self.gap) / self.units_per_em);
    }

    // distance between baseline and highest point
    pub fn getBaseline(self: *const Font, font_size: u16) u16 {
        return @intCast(font_size * self.advance / self.units_per_em);
    }

    pub fn getLineGap(self: *const Font, font_size: u16) u16 {
        return @intCast(font_size * self.gap / self.units_per_em);
    }
};

